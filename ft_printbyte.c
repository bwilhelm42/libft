#include "libft.h"

void	ft_printbyte(unsigned char byte)
{
	unsigned char i;

	i = 128;
	while (i > 0)
	{
		if (i & byte)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		i >>= 1;
	}
}
